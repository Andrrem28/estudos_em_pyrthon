'''
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
string -> stringing ->  texto
stringings são textos que estão dentro de aspas (simples/duplas)
'''

# Uso de aspas simples 
print('string')
print('"string"') # Uso das aspas simples, com duplas.
# Usode aspas duplas
print("string")
print("'string'") # Uso das asplas duplas, com simples.
# Uso de Escape (O escape serve para pular um caractere)
print("str \"ing")
# Uso do r (O r serve para mostringar toda stringing. Ou seja, até o conteúdo do escape)
print(r"str \"ing\"")