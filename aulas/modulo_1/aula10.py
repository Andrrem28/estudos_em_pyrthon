# Peculiaridades usando os operadpres + (adição) e * (multiplicação)
concatenacao = 'An' + 'dré'
print('Ex. de Concatenação')
print(concatenacao)

concatenacao_com_coercao = concatenacao + ' ' + str(36) + ' anos'
print('Ex. de Concatenação com Coersão')
print(concatenacao_com_coercao)

a_dez_vezes = 'A' * 10
print('Repetição de caracteres')
print(a_dez_vezes)
print('Repetição de palavras')
tres_vezes_andre = 3 * 'André'
print(tres_vezes_andre)