'''
Tipos de dados, int e float
int -> Número inteiro
O tipo int representa qualquer número
inteiro, sendo ele positivo ou negativo. 
int sem sinal é considerado positivo.
'''
print("Números de tipo int")
print(11) # int
print(-11) # int
print(0) # int

'''
float -> Número com ponto flutuante
O tipo float, representa qualquer número
positivo ou negativo com ponto flutuante.
float sem sinal é considerado positivo.
'''
print("Números de tipo float")
print(1.1) # float
print(-1.1) # float
print(-0.0) # float

# A função type mostra o tipo que o Python
# Obs: type não é uma função e sim uma Class. Mas, neste exemplo é tratado como uma função.
# inferiu ao valor.
print(type(1), type(1.1))    
